def count_letters(str):
    str_lower = str.lower()
    characters = {}
    for character in str_lower:
        if character.isalpha():
            if not(character in characters):
                characters[character] = 1
            else:
                characters[character] += 1
    return characters


# TODO Напишите функцию calculate_frequency
def calculate_frequency(dict):
    sum = 0
    dict_freq = dict
    for i in dict_freq.values():
        sum += i
    for key in dict_freq:
        dict_freq[key] /= sum
    return dict_freq


